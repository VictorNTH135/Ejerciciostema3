//Primera parte:Hacer una función con 3 parametros que se sumen entre sí & llamar la función para darle valores
public class ejerciciot3 {

    public static int suma3nu(int nu1, int nu2, int nu3) {
        int suma = nu1 + nu2 + nu3;
        return suma;
    }

    public static void main(String[] args) {
        int resultado = suma3nu(67, 54, 21);
        System.out.println(resultado);
    }
}





